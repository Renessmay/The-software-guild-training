public class Capital {
    private String name;
    private int pop;
    private int mile;
    
    
    public Capital(){
        this.name=name;
        this.pop=pop;
        this.mile=mile;
    
}

    Capital(String name) {
         this.name=name;
    }
    public String getname(){
        return name;
    }
    public void setname(String name){
        this.name=name;
    }
    public int getpop(){
        return pop;
    }
    public void setpop(int pop){
        this.pop=pop;
    }
      public int getmile() {
        return mile;
    }
    public void setmile(int mile) {
        this.mile=mile;

}
}
